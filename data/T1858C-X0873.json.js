    "難曰。(一有知不矜難。由前云智有窮幽之鑒而無知焉。神有應會之用而無慮焉。故躡此二句以興難。意謂既有知有會。豈可言無知無會也。但聖人有知而不矜耳)夫聖人真心獨朗。物物斯照。應接無方。動與事會。物物斯照。故知無所遺。(萬境齊觀)動與事會。故會不失機。(有感即赴。上領旨也。下敘計)會不失機故。必有會於可會。(謂必定有機可會)知無所遺故。必有知於可知。(謂必有能知之心。知於可知之境)必有知於可知。故聖不虗知。必有會於可會。故聖不虗會。(謂既有知有會。必有可知可會之境。此則知不虗知。會非虗會矣。下正難)既知既會。而曰無知無會者。何耶。(此正申難也。既有知有會。而曰無者。豈不謬耶。下敘救)若夫忘知遺會者。則是聖人無私於知會。以成其私耳。(此敘救也。謂若以忘知遺會為救者。則是聖人雖有知會。而不自矜恃為己能。返以成其知會之名耳。無私成私。語出老子。後其身而身先。外其身而身存。不自貴愛其身而身返存。謂聖人但以不矜其知會為己私。故人以知會歸之。其實非無知會也)斯可謂不自有其知。安得無知哉。(此轉破也。謂如所救云。聖人不矜恃知會為己長。斯可謂不自有其知。豈得謂無知哉○下約知無知相答)答曰。(約真諦可亡而知以答。先立理)夫聖人功高二儀(仁也)而不仁。(大仁不仁)明逾日月而彌昏。(功高。謂權智應物。明逾等。謂實智證真。無為而應。故不仁。不慮而知。故彌昏)豈曰木石瞽其懷。其於無知而已哉。(此揀異無情也。若謂無知。不同於木石)誠以異於人者神明。故不可以事相求之耳。(此揀異有情也。若謂有知。不同凡夫)子意欲令聖人不自(矜恃)有其知。而聖人未嘗不有知。(此牒審難意。若以此為得者)無乃乖於聖心。失於文旨者乎。(總責不但不知聖心。抑且失於文旨)何者。(徵釋通難)經云。真般若者。清淨如虗空。無知無見。無作無緣。斯則知自無知矣。豈待返照。(絕無)然後無知哉。(此引經證成般若。但無惑取之知。非絕無真知也。義引大品等文。言真般若者。意在離妄。以體絕纖塵。故清淨如空。以無惑取。故無知無見。以非有為。故無作無緣。以此而觀。則般若真知獨照。知自無知耳。豈待泯絕靈明。然後為無知哉)若有知性空而稱淨者。(此牒轉救也。謂若以般若實實有知。但以性空而稱淨者)則不辨於惑智。(此返責也。謂若以般若性空為淨者。則不辨於惑智。以煩惱亦性空。豈稱般若哉)三毒四倒亦皆清淨。有何獨尊於般若。(若謂般若以性空為清淨者。則三毒四倒亦皆性空。如此則真妄不分。有何獨尊於般若哉)若以所知(真諦)美般若。(此敘轉救也。謂若以所知之真諦清淨。以此美般若為清淨者)所知(真諦)非般若。(真諦為所觀之境。般若為能觀之心。心境不一。故非般若)所知自常淨。故般若未嘗淨。(謂若以真諦清淨美般若者。然在真諦體固常淨。今為所觀。則對待未忘。是般若未嘗淨。則是真諦返累於般若)亦無緣(因也)致淨歎於般若。(若真諦有累於般若。則亦無因以淨致歎於般若。下正釋)然經云般若清淨者。將無(豈非)以般若體性真淨。本無惑取之知。本無惑取之知。不可以知名哉。(此正釋經義也。經云般若清淨者。豈非以般若性淨。本無惑取之知。既無惑取之知。是不可以真知名哉。但無妄知。非無真知也。如此)豈唯無知名無知。知自無知矣。(何獨以絕然無知為無知。良以真知自無妄知耳)是以聖人以無知之般若。照彼無相之真諦。真諦無兔馬之遺。般若無不窮之鑒。所以會而不差。當而無是。寂怕無知。而無不知者矣。",
    "此結答問意也。謂能觀之智無知。所觀之理無相。以無知之智。照無相之理。故心境如如。一道齊平。所以理絕三乘之跡。兔馬。三乘淺深之喻也。而般若照徹無餘。故無不窮之鑒。如此。所以權智應會羣機而不差。觸事當理而無是。實智則寂然不動。怕然無為。故無知而無不知矣。聖智如此。豈以不矜其知為無知。又豈絕然無知為無知哉。0345a11"
